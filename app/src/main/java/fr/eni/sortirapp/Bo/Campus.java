package fr.eni.sortirapp.Bo;

public class Campus {
    private int id;
    private String nom;

    public Campus() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }
}
